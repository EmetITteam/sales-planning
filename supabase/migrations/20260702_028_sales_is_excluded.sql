-- Migration 028: sales.is_excluded — окрема колонка для «non-promo» знижок
-- Created 2026-07-02
-- ============================================================================
--
-- Раніше було 2 boolean-колонки:
--   is_ignored — розхідники + косметика EXOXE (товарний рівень)
--   is_gift    — рядки з sum=0 + повод «Подарок»
--
-- Але excluded discount rows (Рекламная продукция / День Рождения / Гонорар /
-- Амбассадор_free) залишалися is_ignored=false, is_gift=false і потрапляли у
-- метрики. Це давало +534 рядки у червні (4805 замість 4271 valid).
--
-- Додаємо is_excluded як окрему колонку. Backfill / nightly sync мають писати
-- це поле, а всі агрегати мають фільтрувати:
--   WHERE NOT is_ignored AND NOT is_gift AND NOT is_excluded
-- ============================================================================

ALTER TABLE sales ADD COLUMN IF NOT EXISTS is_excluded BOOLEAN NOT NULL DEFAULT FALSE;

COMMENT ON COLUMN sales.is_excluded IS
  'true = рядок з non-promo поводом скидки (Рекламная продукция, День Рождения, Гонорар) або Амбассадор_free (sum=0). Не входить у стратегічні метрики.';

-- Індекс не робимо — фільтр іде разом з is_ignored/is_gift, які вже покриті
-- існуючими партіальними індексами.
