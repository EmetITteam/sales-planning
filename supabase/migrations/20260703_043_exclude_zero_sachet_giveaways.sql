-- Migration 043: mark $0 sachet giveaways as excluded (data correction)
-- Created 2026-07-03
-- ============================================================================
--
-- $0-саше (пробники ESSE Foundation/Moisturiser 1ml sachet тощо) — це РОЗДАЧА
-- семплів, не продаж. Раніше рахувались як продажі (роздували к-ть клієнтів/шт,
-- занижали середній чек), бо мали розпізнаний бренд → IGNORE не спрацьовував.
--
-- Правило (ITD 2026-07-03): $0 + «саше»/«sachet» у назві → is_excluded=true.
-- ТІЛЬКИ $0 — платні міні-продукти (sum>0) лишаються продажами (їх ~35к, НЕ чіпаємо).
-- Не чіпаємо подарункові рядки (is_gift) — вони окремо.
--
-- Класифікатор (sales-classifier.ts isSachetGiveaway) вже робить це для НОВИХ
-- рядків; ця міграція виправляє ІСНУЮЧІ ~10k рядків. Оборотно:
--   UPDATE sales SET is_excluded=false WHERE ... (той самий фільтр) — але тоді
--   повернуться і ті, що були excluded раніше; для точного відкату лишаємо
--   batch_id-незалежний фільтр вузьким (саме $0 саше).
-- ============================================================================

UPDATE sales
SET is_excluded = true
WHERE sum_usd = 0
  AND NOT is_gift
  AND NOT is_excluded
  AND (product ILIKE '%саше%' OR product ILIKE '%sachet%');
