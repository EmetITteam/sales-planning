-- Акція «1$ + 1 шот IUSE Collagen»: клієнту дарували безкоштовний шот
-- («IUSE Marine Collagen - 1 шот», sum=$0) до покупки. Клієнти, що отримали
-- ЛИШЕ безкоштовний шот, помилково рахувались покупцями → IUSE Coll.·КЦ роздутий
-- (726 «клієнтів» замість 226 реальних покупців за H1 → прогноз зашкалює).
--
-- Правило (ITD 2026-07-03), те саме що для $0-саше (043): $0 + «шот» → excluded.
-- ТІЛЬКИ $0 — платні упаковки «30 шотів» ($75+, ~2459 рядків) НЕ чіпаємо.
-- Не чіпаємо подарункові рядки (is_gift).
--
-- Класифікатор (isZeroPriceGiveaway) вже робить це для НОВИХ рядків; ця міграція
-- виправляє існуючі ~880 рядків.
-- ============================================================================

UPDATE sales
SET is_excluded = true
WHERE sum_usd = 0
  AND NOT is_gift
  AND NOT is_excluded
  AND product ILIKE '%шот%';
