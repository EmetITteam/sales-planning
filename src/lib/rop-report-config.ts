/**
 * Конфіг Зведеного звіту РОП (Лист 4) — адресати та периметр регіонів у ОДНОМУ
 * місці (щоб не було інлайн-рядків/умов по коду).
 */

/**
 * Кому ПОДАЄТЬСЯ сам звіт (заголовок, тексти). За Регламентом — «звітування РОП
 * перед CSO і CMO».
 */
export const REPORT_RECIPIENT = 'CSO/CMO';

/**
 * Кому ЕСКАЛЮЄТЬСЯ бренд у «відставанні» в 4+ регіонах (секція 4.2). Це продукт+
 * маркетинг (CPO+CMO) — інший адресат, ніж сам звіт: проблема бренда в багатьох
 * регіонах вирішується на рівні продукту, не лише продажів (регламент 4.2).
 */
export const ESCALATION_RECIPIENT = 'CPO/CMO';

/**
 * Представництва = регіони з БУКВЕНИМ кодом 1С (DNP, KYV, ODS, KHR, VNN, ZTM,
 * NLV, ZPR). Числові ноди (000000060 Коллцентр, 000000082 HRA, 000000014
 * Маркетинг, 000000037 Лазерхауз…) — службові, НЕ представництва, у звіт РОП не
 * входять. Правило винесене з інлайну щоб було одне джерело істини периметра.
 */
export function isRepresentativeRegionCode(code: string): boolean {
  return /^[A-Za-z]/.test((code || '').trim());
}
